# MAIN OCR FUNCTION
# ─────────────────────────────────────────────
def convert_handwriting(image, mode, confidence_threshold, output_format):
    if image is None:
        return " Please upload an image first.", None, gr.update(visible=False)

    try:
        start_time = datetime.now()

        # ── PREPROCESSING ──
        processed = preprocess_image(image, mode=mode)

        # ── OCR PASS 1: Preprocessed image ──
        try:
            results1 = reader.readtext(
                processed,
                detail=1,
                paragraph=False,
                contrast_ths=0.1,
                adjust_contrast=0.5,
                text_threshold=0.6,
                low_text=0.3,
                link_threshold=0.3,
            )
        except:
            results1 = []

        # ── OCR PASS 2: Original image (catches what preprocessing may miss) ──
        orig = np.array(image)
        if len(orig.shape) == 3 and orig.shape[-1] == 4:
            orig = cv2.cvtColor(orig, cv2.COLOR_RGBA2RGB)

        try:
            results2 = reader.readtext(
                orig,
                detail=1,
                paragraph=False,
                contrast_ths=0.1,
                adjust_contrast=0.5,
            )
        except:
            results2 = []

        # ── MERGE + DEDUPLICATE ──
        seen_texts = set()
        all_results = []
        threshold = float(confidence_threshold)

        for (bbox, text, prob) in results1 + results2:
            text_clean = text.strip()
            if prob >= threshold and text_clean and text_clean.lower() not in seen_texts:
                seen_texts.add(text_clean.lower())
                all_results.append((bbox, text_clean, prob))

        # Sort by Y position (top to bottom reading order)
        if all_results:
            all_results.sort(key=lambda r: (r[0][0][1] + r[0][2][1]) / 2)

        # ── EXTRACT TEXT ──
        lines = [r[1] for r in all_results]
        raw_text = '\n'.join(lines)
        final_text = clean_text(raw_text, keep_special=True)
        formatted = format_paragraphs(lines)

        if not final_text.strip():
            return (
                " No text detected.\n\n"
                "Tips:\n"
                "• Use a clearer, higher-resolution image\n"
                "• Ensure good lighting — no shadows\n"
                "• Try 'Dark Background' mode if text is light\n"
                "• Make sure text is not too small",
                None,
                gr.update(visible=False)
            )

        # ── STATS ──
        elapsed = (datetime.now() - start_time).total_seconds()
        word_count = len(final_text.split())
        line_count = len([l for l in lines if l.strip()])
        avg_conf = sum(r[2] for r in all_results) / len(all_results) if all_results else 0
        conf_emoji = "" if avg_conf > 0.75 else "" if avg_conf > 0.50 else ""

        stats = (
            f"\n\n{'─'*40}\n"
            f"Stats\n"
            f"  {conf_emoji} Confidence : {avg_conf:.0%}\n"
            f"  Words     : {word_count}\n"
            f"   Lines     : {line_count}\n"
            f"    Time      : {elapsed:.1f}s\n"
            f"{'─'*40}"
        )

        display_text = formatted + stats

        # ── SAVE OUTPUT ──
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = None

        if output_format == "TXT (.txt)":
            output_path = f"ocr_output_{timestamp}.txt"
            with open(output_path, "w", encoding="utf-8") as f:
                f.write(formatted)

        elif output_format == "Word (.docx)":
            output_path = f"ocr_output_{timestamp}.docx"
            doc = Document()
            doc.add_heading("OCR Extracted Text", 0)
            doc.add_paragraph(f"Date: {datetime.now().strftime('%d %B %Y, %H:%M')}")
            doc.add_paragraph(f"Confidence: {avg_conf:.0%} | Words: {word_count}")
            doc.add_paragraph("─" * 40)
            for para in formatted.split('\n\n'):
                if para.strip():
                    doc.add_paragraph(para.strip())
            doc.save(output_path)

        elif output_format == "Both (.txt + .docx)":
            txt_path = f"ocr_output_{timestamp}.txt"
            docx_path = f"ocr_output_{timestamp}.docx"
            with open(txt_path, "w", encoding="utf-8") as f:
                f.write(formatted)
            doc = Document()
            doc.add_heading("OCR Extracted Text", 0)
            doc.add_paragraph(formatted)
            doc.save(docx_path)
            output_path = docx_path

        return display_text, output_path, gr.update(value=formatted, visible=True)

    except Exception as e:
        return f" Error: {str(e)}\n\nPlease try again with a different image.", None, gr.update(visible=False)



# ADVANCED PREPROCESSING
# ─────────────────────────────────────────────
def preprocess_image(image, mode="auto"):
    """
    Multi-stage preprocessing pipeline.
    mode: 'auto' | 'handwritten' | 'printed' | 'dark'
    """
    img = np.array(image)

    # Handle RGBA
    if len(img.shape) == 3 and img.shape[-1] == 4:
        img = cv2.cvtColor(img, cv2.COLOR_RGBA2RGB)

    # Grayscale
    if len(img.shape) == 3:
        gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
    else:
        gray = img

    # Upscale small images for better OCR
    h, w = gray.shape
    if max(h, w) < 1000:
        scale = 1500 / max(h, w)
        gray = cv2.resize(gray, (int(w * scale), int(h * scale)),
                          interpolation=cv2.INTER_CUBIC)

    try:
        # CLAHE — adaptive contrast
        clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8, 8))
        enhanced = clahe.apply(gray)
    except:
        enhanced = gray

    if mode == "dark":
        # Invert dark backgrounds
        enhanced = cv2.bitwise_not(enhanced)

    # Denoise
    try:
        denoised = cv2.fastNlMeansDenoising(enhanced, h=10, templateWindowSize=7, searchWindowSize=21)
    except:
        denoised = enhanced

    # Sharpen
    kernel_sharp = np.array([[0, -1, 0], [-1, 5, -1], [0, -1, 0]])
    sharpened = cv2.filter2D(denoised, -1, kernel_sharp)

    # Deskew
    sharpened = deskew(sharpened)

    # Adaptive threshold (best for handwriting + printed)
    try:
        thresh = cv2.adaptiveThreshold(
            sharpened, 255,
            cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
            cv2.THRESH_BINARY,
            31, 10
        )
    except:
        thresh = sharpened

    # Morphological cleanup — remove noise dots
    kernel = np.ones((2, 2), np.uint8)
    cleaned = cv2.morphologyEx(thresh, cv2.MORPH_CLOSE, kernel)

    return cleaned


def deskew(image):
    """Fix tilted/rotated text images."""
    try:
        coords = np.column_stack(np.where(image < 128))
        if len(coords) < 100:
            return image
        angle = cv2.minAreaRect(coords)[-1]
        if angle < -45:
            angle = -(90 + angle)
        else:
            angle = -angle
        if abs(angle) > 30:
            return image  # Too extreme — skip
        (h, w) = image.shape
        M = cv2.getRotationMatrix2D((w // 2, h // 2), angle, 1.0)
        rotated = cv2.warpAffine(
            image, M, (w, h),
            flags=cv2.INTER_CUBIC,
            borderMode=cv2.BORDER_REPLICATE
        )
        return rotated
    except:
        return image


# ─────────────────────────────────────────────
# TEXT CLEANING
# ─────────────────────────────────────────────
def clean_text(text, keep_special=False):
    """Clean extracted text — keep Urdu + English."""
    # Keep: Urdu (0600-06FF), English, digits, punctuation
    if keep_special:
        pattern = r'[^\u0600-\u06FFa-zA-Z0-9\s.,!?:;\-\'\"()\n]'
    else:
        pattern = r'[^\u0600-\u06FFa-zA-Z0-9\s.,!?]'

    text = re.sub(pattern, ' ', text)
    text = re.sub(r'[ \t]+', ' ', text)      # Multiple spaces → single
    text = re.sub(r'\n{3,}', '\n\n', text)   # Triple newlines → double
    return text.strip()


def format_paragraphs(lines):
    """Group lines into paragraphs intelligently."""
    paragraphs = []
    current = []
    for line in lines:
        line = line.strip()
        if not line:
            if current:
                paragraphs.append(' '.join(current))
                current = []
        else:
            current.append(line)
    if current:
        paragraphs.append(' '.join(current))
    return '\n\n'.join(paragraphs)


